// 람다 표현식 (Lambda Expression)과 함수 값

// '표현식(expression)'은 값으로 평가될 수 있는 코드 조각을 의미합니다.
// 자바스크립트에서 함수는 그 자체로 '값'으로 취급될 수 있습니다 (일급 함수 또는 일급 객체).

// 1. 함수 표현식 (Function Expression)
// 함수를 변수에 할당하는 방식입니다. 함수에 이름(m1)을 붙일 수도 있고, 생략할 수도 있습니다.
// 여기서는 'm1'이라는 이름을 가진 함수가 정의되었지만, 실제로는 'm' 변수를 통해 호출됩니다.
let m = function m1() {
  return 1; // 1을 반환하는 함수
};

// 익명 함수 표현식 (Anonymous Function Expression)
// 함수에 이름이 없는 경우입니다. 'm1' 변수에 익명 함수를 할당합니다.
let m1 = function () {
  // 이 함수는 아무것도 반환하지 않으므로, 호출 시 undefined를 반환합니다.
};

// 2. 람다 표현식 (Lambda Expression) / 화살표 함수 (Arrow Function) - ES6 문법
// ES6에서 도입된 더 간결한 함수 표현 방식입니다. 주로 익명 함수를 정의할 때 사용됩니다.

// 매개변수가 없고, '3'을 반환하는 가장 간단한 형태의 화살표 함수입니다.
// 본문이 한 줄이므로 'return'과 중괄호 '{}'를 생략할 수 있습니다.
let m2 = () => 3;

// 매개변수가 하나(x)이고, 'x + 1'을 반환하는 화살표 함수입니다.
// 매개변수가 하나일 경우 소괄호 '()'를 생략할 수 있습니다.
let m3 = (x) => x + 1; // 또는 'let m3 = x => x + 1;'

// 매개변수가 여러 개(x, y)이고, 함수 본문이 여러 줄인 화살표 함수입니다.
// 본문이 여러 줄일 경우 중괄호 '{}'를 사용해야 하며, 명시적으로 'return' 키워드를 사용해야 합니다.
let m4 = (x, y) => {
  let r = x + y; // x와 y를 더한 값을 변수 r에 저장
  return r / 2; // r을 2로 나눈 값을 반환
};

// 매개변수가 여러 개(x, y)이고, 본문이 한 줄인 화살표 함수입니다.
// 계산 결과를 바로 반환하므로 중괄호 '{}'와 'return'을 생략할 수 있습니다.
// 가독성을 위해 소괄호 '()'로 감싸는 경우도 있습니다.
let m5 = (x, y) => (x + y) / 2;

// 3. 함수를 다른 함수의 인자로 전달 (콜백 함수)
// 자바스크립트에서 함수가 일급 객체이므로 가능한 기능입니다.

// 'good'이라는 이름을 가진 함수 표현식입니다.
let m6 = function good() {
  return 1;
};

// 'haha' 함수는 하나의 매개변수 'y'를 받습니다.
// 이 'y'는 함수가 될 것으로 예상되며, 'haha' 함수 내부에서 'y()'로 호출됩니다.
function haha(y) {
  y(); // 'y'에 전달된 함수를 실행
}

// 'haha' 함수를 호출하면서 'm6' 함수 자체를 인자로 전달합니다.
// 이 경우 'haha' 함수 내부의 'y()'는 'm6()'이 되어 'm6' 함수가 실행됩니다.
haha(m6);