// 함수(Function)에 대한 예제

// 함수는 특정 작업을 수행하는 코드 블록입니다.
// 코드의 재사용성을 높이고, 프로그램을 모듈화하여 가독성을 좋게 만듭니다.

// 1. 함수 선언문 (Function Declaration)
// 'function' 키워드를 사용하여 함수를 정의하는 전통적인 방식입니다.
// 자바스크립트에서 함수는 '1급 객체(First-class Citizen)'로 취급됩니다.
// 이는 함수를 변수처럼 다룰 수 있다는 의미로, 변수에 할당하거나, 다른 함수의 인자로 전달하거나, 함수에서 반환할 수 있습니다.

// 'f(x) = x + 1' 과 같은 수학적 함수 표현에 비유될 수 있습니다.
// 'f'는 함수의 이름이고, 'x'와 'y'는 이 함수가 호출될 때 받을 매개변수(parameter)입니다.
function f(x, y) {
  // 'return' 키워드는 함수의 실행 결과를 반환합니다.
  // 이 함수는 두 매개변수 x와 y를 더한 값을 반환합니다.
  return x + y;
}

// 함수 호출
// 정의된 함수는 함수 이름 뒤에 괄호 '()' 안에 인자(argument)를 전달하여 실행합니다.
// 자바스크립트는 동적 타입 언어이므로, 숫자와 문자열을 더하면 문자열 결합이 일어납니다.
// 5(number)와 "10"(string)을 더하면 5가 "5"로 암시적으로 변환된 후 "5" + "10" = "510"이 됩니다.
let num = f(5, "10");
console.log(num); // "510" 출력

// 2. 화살표 함수 (Arrow Function) - ES6 문법
// ES6(ECMAScript 2015)에서 도입된 새로운 함수 표현 방식으로,
// 'function' 키워드를 대체하고 더 간결하게 함수를 정의할 수 있게 해줍니다.

// 'const' 키워드를 사용하여 함수를 변수에 할당하는 방식입니다.
// 'add'는 변수 이름이자 함수 이름이 되며, '(x, y)'는 매개변수 목록, '=>'는 화살표 함수의 문법입니다.
const add = (x, y) => {
  // 함수 본문이 여러 줄일 경우 중괄호 '{}'를 사용하고 'return' 키워드를 명시해야 합니다.
  return x + y;
};

// 화살표 함수도 일반 함수처럼 호출할 수 있습니다.
// 예시: let result = add(2, 3); console.log(result); // 5 출력