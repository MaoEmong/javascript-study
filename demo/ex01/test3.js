// 조건문(Conditional Statement)과 반복문(Loop) 예제

// 1. 조건문 (if-else)
// 프로그램의 흐름을 조건에 따라 다르게 제어할 때 사용합니다.
let n1 = 10; // 변수 n1을 10으로 초기화

// 'if' 키워드 뒤의 괄호 안의 조건식이 참(true)이면 'if' 블록 내부의 코드를 실행합니다.
// 조건식이 거짓(false)이면 'else' 블록 내부의 코드를 실행합니다.
// '==' 연산자는 두 피연산자의 값이 같은지 비교합니다 (타입은 비교하지 않음).
if (n1 == 10) {
  // n1이 10과 같으므로 이 블록이 실행됩니다.
  console.log("10입니다"); // "10입니다" 출력
} else {
  // 이 블록은 실행되지 않습니다.
  console.log("10이 아닙니다");
}

// 2. 반복문 (for)
// 특정 코드 블록을 여러 번 반복하여 실행할 때 사용합니다.

// 숫자 배열 선언
let list = [1, 2, 3, 4];

// 2-1. 전통적인 for 반복문 (C-style for loop)
// 초기화(let i = 0): 반복문이 시작될 때 한 번 실행됩니다. 반복 변수 'i'를 0으로 설정합니다.
// 조건(i < list.length): 매 반복 시작 전에 이 조건식을 평가합니다. 참이면 반복을 계속하고, 거짓이면 반복을 종료합니다.
// 증감(i++): 매 반복이 끝날 때마다 실행됩니다. 반복 변수 'i'를 1 증가시킵니다.
for (let i = 0; i < list.length; i++) {
  // 'list' 배열의 'i'번째 요소를 출력합니다.
  console.log(list[i]);
}
console.log("\n"); // 줄바꿈 출력

// 2-2. for...of 반복문 (ES6+)
// 'for...of'는 배열이나 다른 반복 가능한(iterable) 객체(예: 문자열, Map, Set)의 각 요소에 대해 반복합니다.
// 배열의 인덱스에 직접 접근하지 않고, 각 요소를 변수에 직접 할당하여 사용할 수 있어 코드가 간결하고 가독성이 좋습니다.
for (let val of list) {
  // 'list' 배열의 각 요소를 'val' 변수에 할당하며 반복합니다.
  console.log(val);
}